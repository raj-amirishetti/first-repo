package com.first.practice;

public class TypeCastConcept {

	public static void main(String[] args) {
		int a = 'A';// smaller datatype's value to bigger datatype
		System.out.println(a); // Implicit typecasting
		
		
		int x=130;
		byte b=(byte)x; //bigger datatype's value to smaller datatype
		System.out.println(b);//explicit typecasting
		
		
	}
}

